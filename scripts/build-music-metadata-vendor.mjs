import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import * as esbuild from 'esbuild';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outfile = path.join(root, 'js', 'vendor', 'music-metadata-browser.mjs');
const noticeFile = path.join(root, 'js', 'vendor', 'music-metadata-browser.NOTICE.txt');

await mkdir(path.dirname(outfile), { recursive: true });
const result = await esbuild.build({
  entryPoints: [path.join(root, 'node_modules', 'music-metadata', 'lib', 'core.js')],
  outfile,
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: 'es2022',
  minify: true,
  sourcemap: false,
  legalComments: 'eof',
  metafile: true,
  logLevel: 'silent'
});

await writeVendorNotice(result.metafile);

function getBundledPackage(inputPath) {
  const normalized = inputPath.replace(/\\/g, '/');
  const marker = 'node_modules/';
  const markerIndex = normalized.lastIndexOf(marker);
  if (markerIndex === -1) {
    return null;
  }

  const rest = normalized.slice(markerIndex + marker.length).split('/');
  const isScoped = rest[0]?.startsWith('@');
  const nameParts = isScoped ? rest.slice(0, 2) : rest.slice(0, 1);
  if (nameParts.length === 0 || nameParts.some(part => !part)) {
    return null;
  }

  const packageName = nameParts.join('/');
  const packageRoot = path.resolve(root, 'node_modules', ...nameParts);
  return { packageName, packageRoot };
}

async function readPackageJson(packageRoot) {
  try {
    return JSON.parse(await readFile(path.join(packageRoot, 'package.json'), 'utf8'));
  } catch {
    return null;
  }
}

async function readNoticeFiles(packageRoot) {
  const entries = await readdir(packageRoot, { withFileTypes: true });
  const noticeNames = entries
    .filter(entry => entry.isFile())
    .map(entry => entry.name)
    .filter(name => /^(?:licen[cs]e|copying|notice)(?:[.\-_].*)?$/i.test(name))
    .sort((a, b) => a.localeCompare(b, 'en'));

  const notices = [];
  for (const name of noticeNames) {
    const text = (await readFile(path.join(packageRoot, name), 'utf8')).trim();
    if (text) {
      notices.push({ name, text });
    }
  }
  return notices;
}

async function writeVendorNotice(metafile) {
  const packages = new Map();
  for (const inputPath of Object.keys(metafile.inputs)) {
    const bundledPackage = getBundledPackage(inputPath);
    if (bundledPackage) {
      packages.set(bundledPackage.packageName, bundledPackage.packageRoot);
    }
  }

  const sections = [];
  for (const [packageName, packageRoot] of [...packages.entries()].sort(([a], [b]) => a.localeCompare(b, 'en'))) {
    const packageJson = await readPackageJson(packageRoot);
    const notices = await readNoticeFiles(packageRoot);
    const header = [
      packageName,
      packageJson?.version ? `Version: ${packageJson.version}` : null,
      packageJson?.license ? `License: ${packageJson.license}` : null
    ].filter(Boolean).join('\n');

    const noticeText = notices.length > 0
      ? notices.map(({ name, text }) => `${name}\n${text}`).join('\n\n')
      : 'No license file was included in this package.';
    sections.push(`${header}\n\n${noticeText}`);
  }

  const body = [
    'Third-party notices for js/vendor/music-metadata-browser.mjs',
    '',
    'This file is generated by scripts/build-music-metadata-vendor.mjs from the packages bundled into the browser metadata parser.',
    '',
    sections.join('\n\n---\n\n')
  ].join('\n');

  await writeFile(noticeFile, `${body}\n`, 'utf8');
}
